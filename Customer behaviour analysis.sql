--9. Segment customer into new ,returning and loyal based on thier total number of previous purchase and 
-- show the count of each segment

SELECT 
	CASE
		WHEN previous_purchases= 0 THEN 'New Customer'
		WHEN previous_purchases BETWEEN 1 AND 15 THEN 'Returning Customer  '
		ELSE 'Loyal Customer '
	END AS Customer_segment ,
	COUNT(*) AS customer_count
	FROM shopping
	GROUP BY Customer_segment;


--? Business Problem :- Lack of customer segmentation leads to generic strategies


--10 What are the top three most purchased products within each category ?


WITH CTE AS
(
SELECT item_purchased,category,COUNT(item_purchased) AS number_of_purchased,
RANK() OVER(PARTITION BY category ORDER BY COUNT(item_purchased) DESC) AS RNK
FROM shopping
GROUP BY item_purchased,category
)
SELECT * FROM CTE
WHERE RNK<=3;

--?Business Problem :- The company doesn,t know top -performing products within categories.

--11. Are customers who are repeat buyers (more than 5 previous purchase) also likely to subscribe


SELECT CASE
			WHEN previous_purchases>5 THEN 'Repeat Buyers'
			ELSE 'Normal Buyers'
		END AS customer_type,subscription_status,
		COUNT(*) AS customer_count,
		COUNT(*) * 100/ SUM(COUNT(*)) OVER (PARTITION BY CASE
			WHEN previous_purchases>5 THEN 'Repeat Buyers'
			ELSE 'Normal Buyers'
		END ) AS percents
		
		FROM shopping
		GROUP BY customer_type,subscription_status;

--? Business Problem :- Unclear relationship between loyality and subscription 

