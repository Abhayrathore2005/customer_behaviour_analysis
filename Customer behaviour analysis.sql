--8. Top 5 products with highest discount usage%

SELECT item_purchased ,
COUNT(item_purchased) AS Total_number_of_time_sold,
COUNT(CASE WHEN(discount_applied='Yes') THEN 1 END) AS Number_of_time_sold_when_discount_applied,
